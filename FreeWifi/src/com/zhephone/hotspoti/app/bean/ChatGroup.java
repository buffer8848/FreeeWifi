package com.zhephone.hotspoti.app.bean;

public class ChatGroup {
	private String chatName;
	private String deviceToken;
	private String nid;
	private String content;
	private String lastTime;
	private int hasNoReadMessage; //未读消息,0有,1为没有
	//群组名	
	private String name;
	//群组id
	//mu74ae
	private String imei;
	
	public int getHasNoReadMessage() {
		return hasNoReadMessage;
	}
	public void setHasNoReadMessage(int hasNoReadMessage) {
		this.hasNoReadMessage = hasNoReadMessage;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getImei() {
		return imei;
	}
	public void setImei(String imei) {
		this.imei = imei;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getLastTime() {
		return lastTime;
	}
	public void setLastTime(String lastTime) {
		this.lastTime = lastTime;
	}
	public String getChatName() {
		return chatName;
	}
	public void setChatName(String chatName) {
		this.chatName = chatName;
	}
	public String getDeviceToken() {
		return deviceToken;
	}
	public void setDeviceToken(String deviceToken) {
		this.deviceToken = deviceToken;
	}
	public String getNid() {
		return nid;
	}
	public void setNid(String nid) {
		this.nid = nid;
	}
	

}
