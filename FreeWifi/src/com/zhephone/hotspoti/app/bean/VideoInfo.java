package com.zhephone.hotspoti.app.bean;

public class VideoInfo {
	private String videoImage; //视频图片
	private String videoUrl;  //视频播放地址
	private String videoId;
	private String name;  //视频名字
	private int videoType;
	private String videoIntroduction;   //视频介绍
	public String getVideoImage() {
		return videoImage;
	}
	public void setVideoImage(String videoImage) {
		this.videoImage = videoImage;
	}
	public String getVideoUrl() {
		return videoUrl;
	}
	public void setVideoUrl(String videoUrl) {
		this.videoUrl = videoUrl;
	}
	public String getVideoId() {
		return videoId;
	}
	public void setVideoId(String videoId) {
		this.videoId = videoId;
	}
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getVideoType() {
		return videoType;
	}
	public void setVideoType(int videoType) {
		this.videoType = videoType;
	}
	public String getVideoIntroduction() {
		return videoIntroduction;
	}
	public void setVideoIntroduction(String videoIntroduction) {
		this.videoIntroduction = videoIntroduction;
	}
	

}
